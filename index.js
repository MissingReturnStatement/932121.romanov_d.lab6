
function left() {
    document.querySelector('.cat').style.width = "100%";
    document.querySelector('.dog').style.width = "30px";
}
function both() {
    document.querySelector('.cat').style.width = "50%";
    document.querySelector('.dog').style.width = "50%";
}
function right() {
    document.querySelector('.dog').style.width = "100%";
    document.querySelector('.cat').style.width = "30px";
}
